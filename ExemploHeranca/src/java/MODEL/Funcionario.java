package MODEL;

/**
 *
 * @author gusta
 */
public class Funcionario {
    
    private String nome; 
    private int idade;
    private String departamento;
    private String cargo; 
    
    public Funcionario(){
        
    }

    public Funcionario(String nome, int idade, String departamento, String cargo) {
        this.nome = nome;
        this.idade = idade;
        this.departamento = departamento;
        this.cargo = cargo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getDepartamento() {
        return departamento;
    }

    public void setDepartamento(String departamento) {
        this.departamento = departamento;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }
    
    
}
